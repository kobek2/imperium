/**
 * Pick the next archetype to docket, avoiding the most recent N case archetypes.
 * Returns the archetype key or null if all are recently used (caller falls back to no-op).
 */
export function pickNextArchetype(recentArchetypeKeys: string[], seed: string): string | null {
  const recent = new Set(recentArchetypeKeys);
  const pool = ARCHETYPES.filter((a) => !recent.has(a.key));
  if (pool.length === 0) {
    // All archetypes have been used recently — allow recycling the oldest.
    if (recentArchetypeKeys.length === 0) return null;
    return recentArchetypeKeys[recentArchetypeKeys.length - 1] ?? null;
  }
  const ix = Math.floor(hashToUnit(seed) * pool.length) % pool.length;
  return pool[ix]!.key;
}
