/**
 * Attorney General court docket: archetype catalog, agenda generation, validation, scoring.
 *
 * Mirrors the shape of `diplomatic-story.ts` for the Secretary of State:
 *   - A library of authorable scripts (here, *archetypes*) hand-tuned for RP.
 *   - A `buildCourtAgenda(...)` function that materializes the agenda the AG actually argues.
 *   - `validateArgumentChoices` + `scoreCourtOutcome` keep all decisions server-side reproducible.
 *
 * The AG always argues the *government's* side. They pick whether the United States is defending
 * the existing legal order on the issue (`defend`) or challenging it (`challenge`). The agenda
 * morphs the position label and opposing argument accordingly; the frame menu, rebuttals, justice
 * questions, and amicus stances are written to be agnostic to side so a single bespoke set carries
 * across both postures and the *side strength* modifier reflects whether they took the
 * archetypally-strong position.
 */

// ---------- 1) Frame catalog (shared across all archetypes) ----------

