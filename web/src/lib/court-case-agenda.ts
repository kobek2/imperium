// ---------- 4) Agenda construction ----------

function hashToUnit(input: string): number {
  let h = 2166136261;
  for (let i = 0; i < input.length; i += 1) {
    h ^= input.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return (h >>> 0) / 2 ** 32;
}

function pickFrames(archetype: Archetype): [CourtFrame, CourtFrame, CourtFrame] {
  const [k0, k1, k2] = archetype.frameKeys;
  const f0 = FRAME_TEMPLATES[k0];
  const f1 = FRAME_TEMPLATES[k1];
  const f2 = FRAME_TEMPLATES[k2];
  return [
    { key: f0.key, label: f0.label, lead: f0.leadPrefix(archetype.defendPosition) },
    { key: f1.key, label: f1.label, lead: f1.leadPrefix(archetype.defendPosition) },
    { key: f2.key, label: f2.label, lead: f2.leadPrefix(archetype.defendPosition) },
  ];
}

function pickRebuttals(
  archetype: Archetype,
): [
  [CourtRebuttal, CourtRebuttal, CourtRebuttal],
  [CourtRebuttal, CourtRebuttal, CourtRebuttal],
  [CourtRebuttal, CourtRebuttal, CourtRebuttal],
] {
  const result: CourtRebuttal[][] = [];
  for (let i = 0; i < 3; i += 1) {
    const frame = FRAME_TEMPLATES[archetype.frameKeys[i]!];
    const rebs = frame.rebuttals.map((r, idx) => ({
      id: `${frame.key}-r${idx}`,
      line: r.line,
      quality: r.q,
    }));
    result.push(rebs);
  }
  return result as [
    [CourtRebuttal, CourtRebuttal, CourtRebuttal],
    [CourtRebuttal, CourtRebuttal, CourtRebuttal],
    [CourtRebuttal, CourtRebuttal, CourtRebuttal],
  ];
}

function buildJustices(archetype: Archetype): [CourtJusticeQuestion, CourtJusticeQuestion, CourtJusticeQuestion] {
  return archetype.justices.map((jq, jix) => ({
    justiceName: jq.name,
    alignment: jq.alignment,
    question: jq.question,
    responses: jq.responses.map((r, rix) => ({
      id: `j${jix}-r${rix}`,
      line: r.line,
      quality: r.quality,
      tone: r.tone,
    })) as [CourtResponse, CourtResponse, CourtResponse],
  })) as [CourtJusticeQuestion, CourtJusticeQuestion, CourtJusticeQuestion];
}

function buildAmicus(archetype: Archetype): [CourtAmicusOption, CourtAmicusOption, CourtAmicusOption] {
  return archetype.amicusOptions.map((a, ix) => ({
    id: `am-${ix}`,
    line: a.line,
    quality: a.quality,
  })) as [CourtAmicusOption, CourtAmicusOption, CourtAmicusOption];
}

/**
 * Materialize the agenda for a case. Same agenda content for either side; the AG's chosen
 * `side_taken` is captured in the case row, not the agenda — agenda is what the AG sees,
 * and identical questions/frames work for both postures with side strength applied at scoring time.
 */
export function buildCourtAgenda(archetypeKey: string, seed: string): CourtAgenda | null {
  const archetype = ARCHETYPES.find((a) => a.key === archetypeKey);
  if (!archetype) return null;

  const tiltCoin = hashToUnit(`${seed}:${archetype.key}:tilt`);
  const tiltParty: "D" | "R" | null = tiltCoin < 0.4 ? "D" : tiltCoin < 0.8 ? "R" : null;

  return {
    agendaVersion: 1,
    archetypeKey: archetype.key,
    topic: archetype.topic,
    caseLabel: archetype.caseLabel,
    plaintiff: archetype.plaintiff,
    defendant: archetype.defendant,
    factPattern: archetype.factPattern,
    questionPresented: archetype.questionPresented,
    defendPosition: archetype.defendPosition,
    challengePosition: archetype.challengePosition,
    defendOpposingArgument: archetype.defendOpposingArgument,
    challengeOpposingArgument: archetype.challengeOpposingArgument,
    defendStrongSide: archetype.defendStrongSide,
    frames: pickFrames(archetype),
    rebuttals: pickRebuttals(archetype),
    justices: buildJustices(archetype),
    amicusOptions: buildAmicus(archetype),
    tiltParty,
  };
}

export function isCourtAgenda(agenda: unknown): agenda is CourtAgenda {
  if (!agenda || typeof agenda !== "object") return false;
  const a = agenda as Partial<CourtAgenda>;
  return (
    a.agendaVersion === 1 &&
    typeof a.archetypeKey === "string" &&
    Array.isArray(a.frames) &&
    a.frames.length === 3 &&
    Array.isArray(a.justices) &&
    a.justices.length === 3 &&
    Array.isArray(a.amicusOptions) &&
    a.amicusOptions.length === 3
  );
}

// ---------- 5) Validation ----------

export function validateArgumentChoices(agenda: CourtAgenda, choicePath: number[]): boolean {
  // [frameIdx, rebuttalIdx, q0, q1, q2]
  if (choicePath.length !== 5) return false;
  const [f, r, q0, q1, q2] = choicePath;
  if (typeof f !== "number" || f < 0 || f > 2) return false;
  if (typeof r !== "number" || r < 0 || r > 2) return false;
  if (typeof q0 !== "number" || q0 < 0 || q0 > 2) return false;
  if (typeof q1 !== "number" || q1 < 0 || q1 > 2) return false;
  if (typeof q2 !== "number" || q2 < 0 || q2 > 2) return false;
  return true;
}

export function validateAmicusChoice(agenda: CourtAgenda, amicusIdx: number): boolean {
  return typeof amicusIdx === "number" && amicusIdx >= 0 && amicusIdx < agenda.amicusOptions.length;
}

// ---------- 6) Scoring ----------

export type CourtArgumentScore = {
  outcomeTier: CourtOutcomeTier;
  rawScore: number;
  argumentQuality: number;
  sideStrengthMod: number;
  tiltMod: number;
  summary: string;
};

const SIDE_STRENGTH_MOD: Record<SideStrength, { defend: number; challenge: number }> = {
  defend: { defend: +2, challenge: -1 },
  challenge: { defend: -1, challenge: +2 },
  split: { defend: 0, challenge: 0 },
};

function tilt(agenda: CourtAgenda, agParty: "D" | "R" | null): number {
  if (agenda.tiltParty == null || agParty == null) return 0;
  return agenda.tiltParty === agParty ? +2 : -2;
}

function tierFromScore(score: number): CourtOutcomeTier {
  if (score >= 12) return "decisive_win";
  if (score >= 8) return "narrow_win";
  if (score >= 4) return "narrow_loss";
  return "decisive_loss";
}

export function scoreCourtArgument(
  agenda: CourtAgenda,
  side: "defend" | "challenge",
  choicePath: number[],
  agParty: "D" | "R" | null,
): CourtArgumentScore {
  const [f, r, q0, q1, q2] = choicePath;
  const rebuttal = agenda.rebuttals[f as 0 | 1 | 2]![r as 0 | 1 | 2]!;
  const justice0 = agenda.justices[0]!.responses[q0 as 0 | 1 | 2]!;
  const justice1 = agenda.justices[1]!.responses[q1 as 0 | 1 | 2]!;
  const justice2 = agenda.justices[2]!.responses[q2 as 0 | 1 | 2]!;

  const argumentQuality = rebuttal.quality + justice0.quality + justice1.quality + justice2.quality;
  const sideStrengthMod = SIDE_STRENGTH_MOD[agenda.defendStrongSide][side];
  const tiltMod = tilt(agenda, agParty);
  const rawScore = argumentQuality + sideStrengthMod + tiltMod;
  const outcomeTier = tierFromScore(rawScore);

  const summary = describeOutcome(agenda, side, outcomeTier);

  return { outcomeTier, rawScore, argumentQuality, sideStrengthMod, tiltMod, summary };
}

export function scoreCourtAmicus(
  agenda: CourtAgenda,
  amicusIdx: number,
  agParty: "D" | "R" | null,
): CourtArgumentScore {
  const opt = agenda.amicusOptions[amicusIdx]!;
  const tiltMod = tilt(agenda, agParty);
  // Amicus caps out at narrow_win.
  const argumentQuality = opt.quality * 2;
  const sideStrengthMod = 0;
  const rawScore = argumentQuality + tiltMod;
  let outcomeTier: CourtOutcomeTier;
  if (rawScore >= 8) outcomeTier = "narrow_win";
  else if (rawScore >= 5) outcomeTier = "narrow_loss";
  else outcomeTier = "decisive_loss";

  const summary = describeAmicusOutcome(agenda, outcomeTier);

  return { outcomeTier, rawScore, argumentQuality, sideStrengthMod, tiltMod, summary };
}

export function declineToDefendOutcome(agenda: CourtAgenda): { outcomeTier: CourtOutcomeTier; summary: string } {
  return {
    outcomeTier: "decisive_loss",
    summary:
      `At oral argument, no government advocate rose to defend ${agenda.defendPosition}. The bench's questions assumed the absence of a contrary federal position; petitioners' counsel walked the Court through the statutory and constitutional defects without meaningful pushback. The Court accepted plaintiffs' arguments in full and left the United States' prior position without support in precedent. The judgment effectively strikes the government's stance.`,
  };
}

function describeOutcome(agenda: CourtAgenda, side: "defend" | "challenge", tier: CourtOutcomeTier): string {
  const position = side === "defend" ? agenda.defendPosition : agenda.challengePosition;
  switch (tier) {
    case "decisive_win":
      return `During oral argument, the Solicitor General's opening framed ${position} around the government's strongest precedents; rebuttal deflected the opposing side's hypotheticals and the Court's skeptical lines returned largely to administrative practice, not constitutional doubt. In a decisive ruling, the Court embraced that framing: the majority opinion tracks the government's principal themes, adopts its proposed limiting principles, and leaves little daylight for lower courts to second-guess the United States on ${position}.`;
    case "narrow_win":
      return `Argument sessions showed a bench split on ${position}: several Justices pressed the government on remedy and severability, while others worried about unsettling reliance interests if the United States lost. The Court ultimately sided with the government, but the narrow majority hedges—explicitly reserving fact-bound applications, inviting future clarification, and declining to endorse every auxiliary argument the Solicitor General offered.`;
    case "narrow_loss":
      return `The United States pressed ${position} through a structured merits argument—opening, rebuttal, and answers to three rounds of bench questions—but the Court's center held against the government. The majority opinion rejects the United States' core inference while borrowing a few narrower points from the government's brief. A vigorous dissent channels much of the government's reasoning, signaling that ${position} could be relitigated when facts or personnel change.`;
    case "decisive_loss":
      return `From the first question at oral argument, the bench signaled deep skepticism of ${position}; rebuttal could not recover lost ground. The Court's decisive opinion repudiates the government's central statutory theory, narrows or overrules supporting precedents the United States invoked, and closes off several fallback arguments the Solicitor General attempted at the lectern. The judgment leaves the government to refashion policy outside the lines the Court drew.`;
  }
}

function describeAmicusOutcome(agenda: CourtAgenda, tier: CourtOutcomeTier): string {
  switch (tier) {
    case "decisive_win":
    case "narrow_win":
      return `Because the United States appeared only as amicus, argument time was limited, but the Department's brief supplied the vocabulary the Court adopted: the majority opinion repeatedly cites the government's framing of the standard of review and the government-proposed limiting construction. On ${agenda.topic}, the Court's result aligns closely with the amicus position even though the United States was not a principal party in ${agenda.caseLabel}.`;
    case "narrow_loss":
      return `The Department's amicus brief in ${agenda.caseLabel} steered the Court toward a modest, federalism-sensitive resolution; oral colloquy showed several Justices engaging seriously with that roadmap. The final opinion nevertheless resolves the merits against the posture the United States urged—while acknowledging the government's filing and incorporating a few of its narrower factual points. The practical loss is contained but real for DOJ's preferred rule.`;
    case "decisive_loss":
      return `The amicus the United States filed in ${agenda.caseLabel} failed to move the Court's center of gravity: questioning at argument treated the government's suggestions as peripheral, and the controlling opinion distances itself from the Department's reasoning. The judgment leaves the United States' amicus position without meaningful purchase on the merits.`;
  }
}

